/* программа, демонстрирующая работу цикла for*/
public class ForDemo {
    public static void main(String[] args) {
        int count;
        for(count=0;count<5;count++) {
            System.out.println("значение переменной count цикла равно "+ count);
        }
        System.out.println("Готово!");
    }
}
